-- إصلاح تكرار الحضور (race condition) — يُشغَّل مرة واحدة فقط
-- --------------------------------------------------------------------
-- الطريقة:
--   1) افتح Supabase Dashboard -> SQL Editor -> New query
--   2) الصق محتوى الملف ده كله
--   3) اضغط Run
--
-- بيعمل إيه؟
--   أ) يمسح أي تكرارات حضور قديمة (نفس اللاعب + نفس اليوم + نفس
--      الرياضة) ويسيب أول تسجيل بس.
--   ب) ينشئ قيد فريد (unique index) يخلي قاعدة البيانات نفسها ترفض
--      تسجيل الحضور مرتين — حتى لو جهازين سجّلوا في نفس اللحظة.
--      الكود في التطبيق بيتعرّف على الرفض ده ويعرض "مسجّل مسبقاً".
-- ====================================================================

-- (أ) إزالة التكرارات الموجودة حالياً — يحتفظ بأقدم صف في كل مجموعة.
delete from attendance a
using attendance b
where a.ctid > b.ctid
  and a.trainee_id = b.trainee_id
  and a.data->>'date' = b.data->>'date'
  and coalesce(a.data->>'sport', '') = coalesce(b.data->>'sport', '');

-- (ب) القيد الفريد: حضور واحد لكل (لاعب، يوم، رياضة).
--     اللاعب متعدد الرياضات لسه يقدر يحضر رياضتين مختلفتين في نفس اليوم.
create unique index if not exists attendance_one_per_day_sport
on attendance (trainee_id, (data->>'date'), (coalesce(data->>'sport', '')));
